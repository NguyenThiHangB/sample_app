# frozen_string_literal: true

module UsersHelper
  def gravatar_for user, options = {size: Settings.user_helper.size}
    gravatar_id = Digest::MD5.hexdigest user.email.downcase
    size = options[:size]
    gravatar_url = "https://secure.gravatar.com/avatar/#{gravatar_id}?s=#{size}"
    image_tag(gravatar_url, alt: user.name, class: "gravatar")
  end

  def create_index params_page, index, per_page
    params_page = 1 if params_page.nil?
    (params_page.to_i - 1) * per_page.to_i + index.to_i + 1
  end

  def unfollow user
    @user = User.find_by id: params[:id]
    @follower = current_user.active_relationships.find_by followed_id: @user.id
  end
end
